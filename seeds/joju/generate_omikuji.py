# extra（タグ外の付加情報）。各行で巡回させる。
LUCKY_ITEM = ["万年筆", "付箋", "青いノート", "観葉植物", "腕時計", "名刺入れ", "緑茶", "ブックカバー", "鉛筆", "印鑑", "しおり", "白いハンカチ"]
LUCKY_COLOR = ["紺", "白", "緑", "金", "青", "橙", "紫", "銀", "桜色", "山吹色", "藍", "若草色"]
LUCKY_DIR = ["北", "東", "南", "西", "北東", "南東", "南西", "北西"]
LUCKY_NUM = ["3", "7", "8", "1", "5", "9", "2", "6", "4", "11", "21", "24"]

FIELDS = ["jinjya", "fortune", "message", "仕事", "学業", "試験", "対人",
          "ラッキーアイテム", "ラッキーカラー", "吉方位", "開運数字"]


def main():
    out_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "omikuji.csv")
    rows = []
    n = 0
    for fortune, pools in LEVELS.items():
        count = len(pools["message"])
        for i in range(count):
            rows.append({
                "jinjya": JINJYA,
                "fortune": fortune,
                "message": pools["message"][i],
                "仕事": pools["仕事"][i],
                "学業": pools["学業"][i],
                "試験": pools["試験"][i],
                "対人": pools["対人"][i],
                "ラッキーアイテム": LUCKY_ITEM[n % len(LUCKY_ITEM)],
                "ラッキーカラー": LUCKY_COLOR[n % len(LUCKY_COLOR)],
                "吉方位": LUCKY_DIR[n % len(LUCKY_DIR)],
                "開運数字": LUCKY_NUM[n % len(LUCKY_NUM)],
            })
            n += 1

    with open(out_path, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=FIELDS)
        writer.writeheader()
        writer.writerows(rows)

    print(f"wrote {len(rows)} omikuji rows -> {out_path}")


if __name__ == "__main__":
    main()
